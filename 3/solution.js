function traverse(map, [startX, startY], [slopeX, slopeY]) {
  let [currX, currY] = [startX, startY];
  const boundX = map[0].length;
  const boundY = map.length;

  console.log('bounds:', boundX, boundY);

  let numTrees = 0;

  while(map[currY + slopeY]) {

    currX = (currX + slopeX) % boundX;
    currY += slopeY;

    if(map[currY][currX] === '#') {
      numTrees += 1;
    }

  }

  return numTrees;
}

